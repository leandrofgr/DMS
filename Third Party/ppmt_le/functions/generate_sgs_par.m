function generate_sgs_par(params,var_i)

params.input_file = strrep(params.input_file,'\','\\');
params.output_file = strrep(params.output_file,'\','\\');


text = ['                  Parameters for SGSIM\n',...
'                  ********************\n',...
'\n',...
'START OF PARAMETERS:\n',...
params.input_file,'                      - file with data\n',...
'1  2  3  ',num2str(var_i),'  0  0         - columns for X,Y,Z,vr,wt,sec.var.\n',...
num2str(params.min),'      ',num2str(params.max),'               - trimming limits\n',...
'0                             - transform the data (0=no, 1=yes)\n',...
'sgsim.trn                     - file for output trans table\n',...
'0                             - consider ref. dist (0=no, 1=yes)\n',...
'histsmth.out                  - file with ref. dist distribution\n',...
'1  2                          - columns for vr and wt\n',...
num2str(params.min),'    ',num2str(params.max),'                   - zmin,zmax(tail extrapolation)\n',...
'1       ',num2str(params.min),'                   - lower tail option, parameter\n',...
'1      ',num2str(params.max),'                   - upper tail option, parameter\n',...
'1                             -debugging level: 0,1,2,3\n',...
'sgsim.dbg                     -file for debugging output\n',...
params.output_file,'                      -file for simulation output\n',...
'1                             -number of realizations to generate\n',...
num2str(params.cellsx),'    1      1              -nx,xmn,xsiz\n',...
num2str(params.cellsy),'    1      1              -ny,ymn,ysiz\n',...
'1      1     1.0              -nz,zmn,zsiz\n',...
params.seed,'                     -random number seed\n',...
num2str(params.min),'    ',num2str(params.max),'                       -min and max original data for sim\n',...
'20                            -number of simulated nodes to use\n',...
'1                             -assign data to nodes (0=no, 1=yes)\n',...
'1     3                       -multiple grid search (0=no, 1=yes),num\n',...
'0                             -maximum data per octant (0=not used)\n',...
num2str(params.search_radius(1)),'       ',num2str(params.search_radius(2)),'  10.0            -maximum search radii (hmax,hmin,vert)\n',...
' 0.0   0.0   0.0              -angles for search ellipsoid\n',...
num2str(params.search_radius(1)),'       ',num2str(params.search_radius(2)),'    1               -size of covariance lookup table\n',...
'0     0.00   1.0              -ktype: 0=SK,1=OK,2=LVM,3=EXDR,4=COLC\n',...
'../data/ydata.dat             -file with LVM, EXDR, or COLC variable\n',...
'4                             -column for secondary variable\n',...
'1    0.000                               -  nst, nugget effect\n',...
num2str(params.variogram_model),'    1.0  0.0   0.0   0.0             -  it,cc,ang1,ang2,ang3\n',...
num2str(params.range(1)),'       ',num2str(params.range(2)),'        ','1.000 -  a_hmax,a_hmin,a_vert'];
    
fid = fopen(params.sgs_par_file,'wt');
fprintf(fid, text);
fclose(fid);
end

